import Ajv2020 from 'ajv/dist/2020.js';
import fs from 'node:fs/promises';
import path from 'node:path';
import YAML from 'yaml';

const root = path.resolve(new URL('..', import.meta.url).pathname);
const scenariosDir = path.join(root, 'scenarios');
const schemaPath = path.join(root, 'schemas/call-scenario.schema.json');
const outputPath = path.join(root, 'site/generated/scenarios.js');
const checkOnly = process.argv.includes('--check');

const schema = JSON.parse(await fs.readFile(schemaPath, 'utf8'));
const validate = new Ajv2020({ allErrors: true }).compile(schema);
const scenarios = await loadScenarios();
const output = renderModule(scenarios);

if (checkOnly) {
  const current = await fs.readFile(outputPath, 'utf8').catch(() => '');
  if (current !== output) {
    throw new Error('site/generated/scenarios.js is stale. Run npm run scenarios:build.');
  }
  console.log('build-scenarios: ok');
} else {
  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, output, 'utf8');
  console.log(`build-scenarios: wrote ${path.relative(root, outputPath)}`);
}

async function loadScenarios() {
  const files = (await fs.readdir(scenariosDir))
    .filter((file) => file.endsWith('.yml') || file.endsWith('.yaml'))
    .sort();
  if (!files.length) throw new Error('No scenario YAML files found.');

  const entries = [];
  for (const file of files) {
    const source = await fs.readFile(path.join(scenariosDir, file), 'utf8');
    const parsed = YAML.parse(source);
    assertValidScenario(file, parsed);
    entries.push(toRuntimeScenario(parsed));
  }
  assertUnique(entries, 'id');
  assertUnique(entries, 'scenario_intent');
  return entries.sort((left, right) => left.id.localeCompare(right.id));
}

function assertValidScenario(file, parsed) {
  if (!validate(parsed)) {
    const details = validate.errors
      .map((error) => `${error.instancePath || '/'} ${error.message}`)
      .join('; ');
    throw new Error(`${file} failed schema validation: ${details}`);
  }
}

function toRuntimeScenario(entry) {
  return {
    id: entry.id,
    catalogOrder: entry.catalog_order ?? Number.MAX_SAFE_INTEGER,
    scenario_intent: entry.scenario_intent,
    title: entry.title,
    buttonLabel: entry.button_label,
    summary: entry.summary,
    phrase: entry.utterances[0],
    utterances: entry.utterances,
    terms: entry.match.keywords,
    replyText: entry.reply.text,
    actions: entry.frontend_actions,
    workflow: entry.workflow,
  };
}

function assertUnique(entries, field) {
  const seen = new Set();
  for (const entry of entries) {
    if (seen.has(entry[field])) throw new Error(`Duplicate scenario ${field}: ${entry[field]}`);
    seen.add(entry[field]);
  }
}

function renderModule(entries) {
  const ordered = [...entries].sort(compareScenarios);
  return `// Generated by scripts/build-scenarios.mjs. Edit scenarios/*.yml instead.\n`
    + `export const CALL_SCENARIOS = ${freezeExpression(ordered)};\n`;
}

function compareScenarios(left, right) {
  return left.catalogOrder - right.catalogOrder || left.id.localeCompare(right.id);
}

function freezeExpression(value) {
  if (Array.isArray(value)) return `Object.freeze([${value.map((entry) => `\n  ${freezeExpression(entry)}`).join(',')}\n])`;
  if (value && typeof value === 'object') {
    const props = Object.entries(value)
      .map(([key, entry]) => `${JSON.stringify(key)}: ${freezeExpression(entry)}`)
      .join(', ');
    return `Object.freeze({ ${props} })`;
  }
  return JSON.stringify(value);
}
